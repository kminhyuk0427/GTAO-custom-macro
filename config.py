# ========================================
# 매크로 정의
# ========================================
# 형식: '트리거 키': {'keys': ['실행할', '키', '순서'], 'delays': [딜레이1, 딜레이2, ...], 'mode': 모드}
# 
# mode 값:
#   0 = 한 번 누르면 시퀀스 1회만 실행 (꾹 눌러도 1회만)
#   1 = 누르고 있는 동안 연속 반복 실행
#
# delays 값 (선택사항):
#   - 각 키 실행 후 대기할 시간 (초 단위)
#   - delays를 지정하지 않으면 기본값(KEY_RELEASE_DURATION) 사용
#   - delays 개수는 keys 개수와 같아야 함
#
# 사용 가능한 키:
#   - 일반 키: 'a', 'b', '1', '2' 등
#   - 특수 키: 'space', 'enter', 'shift', 'ctrl', 'alt', 'tab' 등
#
# 예시 1: 기본 딜레이 사용
#   '1': {
#       'keys': ['q', 'w'],
#       'mode': 1
#   }
#   -> 1번 키를 누르고 있으면 q, w가 기본 딜레이로 연속 반복
#
# 예시 2: 개별 딜레이 지정
#   '2': {
#       'keys': ['2', '1'],
#       'delays': [0.5, 0.01],
#       'mode': 0
#   }
#   -> 2번 키를 누르면: 2 입력 → 0.5초 대기 → 1 입력 → 0.01초 대기
#
# 예시 3: 복잡한 딜레이 패턴
#   '3': {
#       'keys': ['q', 'w', 'e', 'r'],
#       'delays': [0.1, 0.05, 0.2, 0.01],
#       'mode': 1
#   }
#   -> 3번 키를 누르고 있으면 각 키마다 다른 딜레이로 반복
#
